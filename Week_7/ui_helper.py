"""
helpful functions for the user interface. 

user interface is part of the code that interacts with the user. 

"""

def get_user_string(prompt):
    """
    displays the prompt and returns the string entered by the user.
        input: 
            prompt a string to display to the user.
        returns:
            string entered by the user. 
    """
    return input(prompt)